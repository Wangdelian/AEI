package io.jeasyframework.entity;

import java.util.Date;

/**
 * @author zhoubangfu
 * @email zbfcqtl@163.com
 * @date 2017-8-14 09:44:55
 */
public class DocFolder {
    /**
     * id
     */
    private Long folderId;

    /**
     * 文件夹名
     */
    private String folderName;

    /**
     * 版本号
     */
    private Integer folderVersion;

    /**
     * 文件夹大小
     */
    private Double folderSize;

    /**
     * 创建时间
     */
    private Date createTime;

    /**
     * 创建人id
     */
    private Long creatorId;

    /**
     * 创建人名
     */
    private String creatorName;

    /**
     * 修改时间
     */
    private Date mendTime;

    /**
     * 修改人id
     */
    private Long menderId;

    /**
     * 修改人名
     */
    private String menderName;

    /**
     * 状态 1-显示 0-删除
     */
    private Short isenable;

    /**
     * 上级文件夹id
     */
    private Long parentId;

    private boolean isParent;

    private Object standby2;

    private Object standby3;

    private Object standby4;

    private Object standby5;

    public DocFolder() {
    }

    public DocFolder(String folderName, Date createTime, Long creatorId, String creatorName, Long parentId, boolean isParent) {
        this.folderName = folderName;
        this.createTime = createTime;
        this.creatorId = creatorId;
        this.creatorName = creatorName;
        this.parentId = parentId;
        this.isParent = isParent;
    }

    public Long getFolderId() {
        return folderId;
    }

    public void setFolderId(Long folderId) {
        this.folderId = folderId;
    }

    public String getFolderName() {
        return folderName;
    }

    public void setFolderName(String folderName) {
        this.folderName = folderName;
    }

    public Integer getFolderVersion() {
        return folderVersion;
    }

    public void setFolderVersion(Integer folderVersion) {
        this.folderVersion = folderVersion;
    }

    public Double getFolderSize() {
        return folderSize;
    }

    public void setFolderSize(Double folderSize) {
        this.folderSize = folderSize;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Long getCreatorId() {
        return creatorId;
    }

    public void setCreatorId(Long creatorId) {
        this.creatorId = creatorId;
    }

    public String getCreatorName() {
        return creatorName;
    }

    public void setCreatorName(String creatorName) {
        this.creatorName = creatorName;
    }

    public Date getMendTime() {
        return mendTime;
    }

    public void setMendTime(Date mendTime) {
        this.mendTime = mendTime;
    }

    public Long getMenderId() {
        return menderId;
    }

    public void setMenderId(Long menderId) {
        this.menderId = menderId;
    }

    public String getMenderName() {
        return menderName;
    }

    public void setMenderName(String menderName) {
        this.menderName = menderName;
    }

    public Short getIsenable() {
        return isenable;
    }

    public void setIsenable(Short isenable) {
        this.isenable = isenable;
    }

    public Long getParentId() {
        return parentId;
    }

    public void setParentId(Long parentId) {
        this.parentId = parentId;
    }

    public boolean getIsParent() {
        return isParent;
    }

    public void setIsParent(boolean isParent) {
        this.isParent = isParent;
    }

    public Object getStandby2() {
        return standby2;
    }

    public void setStandby2(Object standby2) {
        this.standby2 = standby2;
    }

    public Object getStandby3() {
        return standby3;
    }

    public void setStandby3(Object standby3) {
        this.standby3 = standby3;
    }

    public Object getStandby4() {
        return standby4;
    }

    public void setStandby4(Object standby4) {
        this.standby4 = standby4;
    }

    public Object getStandby5() {
        return standby5;
    }

    public void setStandby5(Object standby5) {
        this.standby5 = standby5;
    }


    @Override
    public String toString() {
        return "DocFolder{" +
                "folderId=" + folderId +
                ", folderName='" + folderName + '\'' +
                ", folderVersion=" + folderVersion +
                ", folderSize=" + folderSize +
                ", createTime=" + createTime +
                ", creatorId=" + creatorId +
                ", creatorName='" + creatorName + '\'' +
                ", mendTime=" + mendTime +
                ", menderId=" + menderId +
                ", menderName='" + menderName + '\'' +
                ", isenable=" + isenable +
                ", parentId=" + parentId +
                ", isParent=" + isParent +
                ", standby2=" + standby2 +
                ", standby3=" + standby3 +
                ", standby4=" + standby4 +
                ", standby5=" + standby5 +
                '}';
    }
}