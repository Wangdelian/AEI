public class UserSkinEntity {
    //用户皮肤id
    private Long userskinid;
    //皮肤名
    private String userskinname;
    //是否默认，1代表是，0代表不是
    private String isdefault;
    //路径
    private String templatepath;
    //顺序号
    private String seq;

    public void setUserskinid(Long userskinid) {
        this.userskinid = userskinid;
    }

    public void setUserskinname(String userskinname) {
        this.userskinname = userskinname;
    }

    public void setIsdefault(String isdefault) {
        this.isdefault = isdefault;
    }

    public void setTemplatepath(String templatepath) {
        this.templatepath = templatepath;
    }

    public void setSeq(String seq) {
        this.seq = seq;
    }

    public Long getUserskinid() {
        return userskinid;
    }

    public String getUserskinname() {
        return userskinname;
    }

    public String getIsdefault() {
        return isdefault;
    }

    public String getTemplatepath() {
        return templatepath;
    }

    public String getSeq() {
        return seq;
    }
}
